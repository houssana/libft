int		ft_intlen_base(int nb, int base)
{
	long int	n;
	int			l;
	int			b;

	n = nb;
	n = (nb < 0) ? -n : n;
	l = 1;
	b = (long int)base;
	while (n >= b)
	{
		l++;
		n /= b;
	}
	return (l);
}
