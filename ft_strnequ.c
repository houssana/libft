#include <string.h>

int		ft_strnequ(char const *s1, char const *s2, size_t n)
{
	size_t	i;
	size_t	l;

	i = 0;
	l = n;
	if (s1 && s2)
	{
		while (s1[i] == s2[i] && n-- && s1[i] != '\0' && s2[i] != '\0')
			i++;
		return ((s1[i] == '\0' && s2[i] == '\0') || i == l);
	}
	return (0);
}
